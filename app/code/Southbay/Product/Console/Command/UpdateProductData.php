    private function update($values, $storeId, $attr_variant)
    {
        /**
         * @var \Magento\Framework\App\ObjectManager $objectManager
         */
        $objectManager = \Magento\Framework\App\ObjectManager::getInstance();

        /**
         * @var SouthbayProductImportCron $cron
         */
        $cron = $objectManager->get(SouthbayProductImportCron::class);

        /**
         * @var ProductImporter $productImporter
         */
        $productImporter = $objectManager->get(ProductImporter::class);

        $request = [];

        if ($attr_variant === SouthbayProduct::ENTITY_COLOR) {
            $options = [];

            foreach ($values as $item) {
                if (!in_array($item['value'], $options)) {
                    $options[] = $item['value'];
                }
            }

            $options = $productImporter->updateSouthbayColorOptions($options);

            foreach ($values as $item) {
                $request[] = [
                    'store_id' => $storeId,
                    'type' => 'update',
                    'attr' => 'others',
                    'sku' => $item['sku'],
                    'product' => [
                        "$attr_variant" => $options[$item['value']]->getValue()
                    ]
                ];
            }
        } else {
            foreach ($values as $item) {
                $request[] = [
                    'store_id' => $storeId,
                    'type' => 'update',
                    'attr' => 'others',
                    'sku' => $item['sku'],
                    'product' => [
                        'sku' => $item['sku'],
                        "$attr_variant" => $item['value']
                    ]
                ];
            }
        }

        $cron->sendRequestFromMemory($request, $storeId, false);
    }

