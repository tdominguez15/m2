    public function __construct()
    {
        parent::__construct();
    }

    protected function configure()
    {
        $this->setName('southbay:fix:future:orders')
            ->setDescription('Fix futures future orders');
    }

    protected function execute(InputInterface $input, OutputInterface $output)
    {
        $stores = [2, 4, 8, 10];

        foreach ($stores as $store_id) {
            $this->updateByStore($store_id, $output);
        }

        return 1;
    }

    private function updateByStore($store_id, OutputInterface $output)
    {
        $output->writeln('#######################################################################################');
        $output->writeln('STORE: ' . $store_id);

        /**
         * @var \Magento\Framework\App\ObjectManager $objectManager
         */
        $objectManager = \Magento\Framework\App\ObjectManager::getInstance();

        /**
         * @var \Magento\Framework\App\ResourceConnection $resource
         */
        $resource = $objectManager->get(\Magento\Framework\App\ResourceConnection::class);
        $connection = $resource->getConnection();

        /**
         * @var \Magento\Sales\Model\ResourceModel\Order\CollectionFactory $collectionFactory
         */
        $collectionFactory = $objectManager->get(\Magento\Sales\Model\ResourceModel\Order\CollectionFactory::class);

        $map = [];
        $season_id = 0;
        if ($store_id == 2) {
            $map = $this->getMapArgentina();
            $season_id = 37;
        } else if ($store_id == 4) {
            $map = $this->getMapUruguay();
            $season_id = 38;
        } else if ($store_id == 8 || $store_id == 10) {
            $map = $this->getMapFrontera();
            if ($store_id == 8) {
                $season_id = 39;
            } else {
                $season_id = 40;
            }
        }

        /**
         * @var \Magento\Sales\Model\ResourceModel\Order\Collection $collection
         */
        $collection = $collectionFactory->create();
        $collection->addFieldToFilter('store_id', ['eq' => $store_id]);
        $collection->addFieldToFilter('ext_order_id', ['eq' => $season_id]);
        $collection->addFieldToFilter('status', ['neq' => Order::STATE_CANCELED]);
        $collection->load(true, false);

        $output->writeln("");
        $output->writeln("Total orders: " . $collection->count());

        $items = $collection->getItems();

        /**
         * @var \Magento\Sales\Model\Order $order
         */
        foreach ($items as $order) {
            $order_id = $order->getId();
            $sql = "SELECT item_id, sku, product_options  FROM sales_order_item WHERE order_id = $order_id";
            $order_items = $connection->fetchAll($sql);

            foreach ($order_items as $item) {
                $_sku = explode('/', $item['sku']);
                $sku = strtoupper($_sku[0]);

                if (isset($map[$sku])) {
                    $flow = $map[$sku];
                    if ($flow == 1) {
                        continue;
                    } else {
                        $product_options = json_decode($item['product_options'], true);

                        if ($product_options && !empty($product_options)) {
                            if (!$product_options || isset($product_options['ignore']) || isset($product_options['info_buyRequest']['ignore'])) {
                                continue;
                            }

                            $info = $product_options['info_buyRequest'];

                            if (
                                !isset($info['month_delivery_date_1']) &&
                                !isset($info['month_delivery_date_2']) &&
                                !isset($info['month_delivery_date_3'])
                            ) {
                                continue;
                            }

                            if (!isset($info['month_delivery_date_1'])) {
                                $info['month_delivery_date_1'] = [];
                            }

                            if (!isset($info['month_delivery_date_2'])) {
                                $info['month_delivery_date_2'] = [];
                            }

                            if (!isset($info['month_delivery_date_3'])) {
                                $info['month_delivery_date_3'] = [];
                            }

                            $total_flow_1 = $this->getTotalFromFlow($info, 'month_delivery_date_1');
                            $total_flow_2 = $this->getTotalFromFlow($info, 'month_delivery_date_2');
                            $total_flow_3 = $this->getTotalFromFlow($info, 'month_delivery_date_3');
                            $has_change = false;

                            if ($flow == 2) {
                                if ($total_flow_1 > 0) {
                                    $has_change = true;
                                    $info = $this->moveFlows($info, 'month_delivery_date_1', 'month_delivery_date_2');
                                }
                            } else if ($flow == 3) {
                                if ($total_flow_1 > 0) {
                                    $has_change = true;
                                    $info = $this->moveFlows($info, 'month_delivery_date_1', 'month_delivery_date_3');
                                }

                                if ($total_flow_2 > 0) {
                                    $has_change = true;
                                    $info = $this->moveFlows($info, 'month_delivery_date_2', 'month_delivery_date_3');
                                }
                            }

                            if ($has_change) {
                                $item_id = $item['item_id'];

                                $output->writeln($order->getIncrementId() . " [$item_id] -- sku: $sku; real flow: $flow; total 1: $total_flow_1; total 2: $total_flow_2; total 3: $total_flow_3;");
                                $output->writeln('before');
                                $output->writeln(json_encode($product_options));

                                $total_flow_1 = $this->getTotalFromFlow($info, 'month_delivery_date_1');
                                $total_flow_2 = $this->getTotalFromFlow($info, 'month_delivery_date_2');
                                $total_flow_3 = $this->getTotalFromFlow($info, 'month_delivery_date_3');

                                $product_options['info_buyRequest'] = $info;

                                $output->writeln('after');
                                $output->writeln(json_encode($product_options));

                                $output->writeln($order->getIncrementId() . " [$item_id] ++ sku: $sku; real flow: $flow; total 1: $total_flow_1; total 2: $total_flow_2; total 3: $total_flow_3;");
                                $json = json_encode($product_options);

                                $sql_update = "UPDATE sales_order_item SET product_options = '$json' WHERE item_id = $item_id";
                                $connection->query($sql_update);
                            }
                        }
                    }
                }
            }
        }
        $output->writeln('#######################################################################################');
    }

    private function moveFlows($info, $from_flow, $to_flow)
    {
        $result = [];
        $result[$from_flow] = [];
        $result[$to_flow] = [];

        $from = $info[$from_flow];
        $to = $info[$to_flow];

        foreach ($from as $key => $value) {
            $result[$from_flow][$key] = 0;

            if (!isset($to[$key])) {
                $to[$key] = 0;
            }

            if (!isset($result[$to_flow][$key])) {
                $result[$to_flow][$key] = $to[$key];
            }

            $result[$to_flow][$key] += $value;
        }

        $info[$from_flow] = $result[$from_flow];
        $info[$to_flow] = $result[$to_flow];

        return $info;
    }

    private function getTotalFromFlow($info, $flow)
    {
        $total = 0;
        $data = $info[$flow];

        foreach ($data as $key => $value) {
            $total += $value;
        }

        return $total;
    }

